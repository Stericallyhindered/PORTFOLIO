// Convert state name to two-letter code
export function getStateCode(state: string): string {
  // If it's already a two-letter code, return it
  if (/^[A-Z]{2}$/.test(state)) return state
  // Try to find it in the map (case-insensitive)
  const stateKey = Object.keys(stateMap).find((key) => key.toLowerCase() === state.toLowerCase())
  return stateKey ? stateMap[stateKey] : state
}
